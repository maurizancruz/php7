<?php 
// Operadores de comparação
	$a = 55.0;
	$b = 55;

	var_dump($a > $b); // Se $a é maior que 

	var_dump($a >= $b); // Se $a é maior que $b

	var_dump($a < $b); // Se $a é menor que 

	var_dump($a <= $b); // Se $a é menor que $b

	var_dump($a == $b); // Compara os valores
	
	var_dump($a === $b); // Compara os valores e tipos = Igual de identidade

	var_dump($a = $b); // $a passa ter o valor de 

	var_dump($a != $b); // Se o valor de $a é difernte de $b
	
	var_dump($a !== $b); // Se o valor eo tipo de dado de $a são diferntes ou iguais a $b



 ?>